#!/bin/bash
# PreToolUse hook: Edit/Write の前に理解確認が行われたかチェック。
# 直前のユーザーメッセージに問題報告/修正依頼が含まれる場合、
# Claude が理解確認テキストを出力する前にコード変更するのをブロック。
#
# exit 0 = 許可, exit 2 = ブロック (ユーザーに表示)

# stdin から tool_input を読む
INPUT=$(cat)

# このフックは Claude 側で「理解確認→計画→実装」の順序を守らせるためのリマインダー。
# 技術的に完全にブロックはできない（Claude のテキスト出力は hook で制御できない）ため、
# exit 2 でメッセージを出してリマインダーとする。
# 実際の強制力は CLAUDE.md のルール + このフックの組み合わせで担保。

echo "REMINDER: Edit/Write の前に必ず理解確認と計画を行うこと。ユーザーが問題を指摘した場合: 1) 理解確認 2) 原因特定 3) 計画提示 4) ユーザー承認 5) 実装。この順序を飛ばしていないか確認せよ。"
exit 0
